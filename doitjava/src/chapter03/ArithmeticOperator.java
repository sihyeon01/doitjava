package chapter03;

public class ArithmeticOperator {

	public static void main(String[] args) {
			int intResult;
			double dRes;
			
			intResult = 3+2;
			System.out.println(intResult);
			intResult = 3-2;
			System.out.println(intResult);
			intResult = 3*2;
			System.out.println(intResult);
			intResult = 3/2;
			System.out.println(intResult);
			dRes = 3.0/2.0;
			System.out.println(dRes);
			intResult = 3%2;
			System.out.println(intResult);
			
			dRes = (double)3/2;
			System.out.println(dRes);
		
	}

}
